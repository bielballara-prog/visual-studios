# Demanem a l'usuari la base del rectangle
base = float(input("Introdueix la base del rectangle: "))

# Demanem a l'usuari l'altura del rectangle
altura = float(input("Introdueix l'altura del rectangle: "))

# Calculem l'àrea del rectangle
area = base * altura

# Calculem el perímetre del rectangle
perimetre = 2 * (base + altura)

# Mostrem l'àrea per pantalla
print("L'àrea del rectangle és:", area)

# Mostrem el perímetre per pantalla
print("El perímetre del rectangle és:", perimetre)